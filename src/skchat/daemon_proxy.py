def _iso(ts) -> str | None:
    if ts is None:
        return None
    return ts.isoformat() if hasattr(ts, "isoformat") else str(ts)


def _msg_to_app(m, *, self_id: str) -> dict:
    """Map a stored ``ChatMessage`` to the app's message JSON.

    Emits the **full shared message contract** the Flutter half builds to:
    id, conversation_id, sender, content_type (short wire form), body, rich, ts,
    reply_to_id, thread_id, edited_at, edit_history, reactions, receipts —
    plus the legacy app fields (peer_id, content, timestamp, is_outbound,
    is_agent, sender_name, delivery_status, is_encrypted) so the existing
    webui/app keep working. ``body`` is ALWAYS present (Golden rule): even an
    unknown ``content_type`` carries a human-readable fallback.
    """
    from skchat.models import ContentType

    sender = getattr(m, "sender", "") or ""
    recipient = getattr(m, "recipient", "") or ""
    outbound = not _is_lumina(sender) and _is_lumina(recipient)
    # The peer of the conversation = whichever party is Lumina.
    peer = LUMINA_ID if (_is_lumina(sender) or _is_lumina(recipient)) else recipient
    ts_iso = _iso(getattr(m, "timestamp", None)) or _now_iso()
    is_agent = _is_lumina(sender)
    body = getattr(m, "content", "") or ""

    # reactions → {emoji: [sender, ...]} (or None if none)
    reactions_map = None
    if hasattr(m, "reactions_map"):
        rm = m.reactions_map()
        reactions_map = rm or None

    # receipts → {delivered:[...], read:[...]} (or None)
    receipts = None
    rcpt = getattr(m, "receipts", None)
    if rcpt is not None:
        receipts = {"delivered": list(rcpt.delivered), "read": list(rcpt.read)}

    # edit_history → [{body, ts}] (or None)
    edit_history = None
    eh = getattr(m, "edit_history", None)
    if eh:
        edit_history = [{"body": e.body, "ts": _iso(e.ts)} for e in eh]

    return {
        # --- shared message contract (Flutter builds to this exact shape) ---
        "id": getattr(m, "id", ""),
        "conversation_id": peer,
        "sender": sender,
        "content_type": ContentType.to_wire(getattr(m, "content_type", "markdown")),
        "body": body,
        "rich": getattr(m, "rich", None),
        "ts": ts_iso,
        "reply_to_id": getattr(m, "reply_to_id", None),
        "thread_id": getattr(m, "thread_id", None),
        "edited_at": _iso(getattr(m, "edited_at", None)),
        "edit_history": edit_history,
        "reactions": reactions_map,
        "receipts": receipts,
        # --- legacy app fields (kept for back-compat) ---
        "peer_id": peer,
        "content": body,
        "timestamp": ts_iso,
        "is_outbound": outbound,
        "delivery_status": "sent",
        "is_encrypted": False,
        "is_agent": is_agent,
        "sender_name": LUMINA_NAME if is_agent else "You",
    }


def _group_msg_to_app(m, *, group_id: str) -> dict:
    """Map a stored group ``ChatMessage`` to the app's message JSON.

    Same shared contract as ``_msg_to_app`` but the ``conversation_id`` is the
    group id and directionality is "outbound iff the operator authored it".
    ``sender_name`` is the member's short handle (so the bubble shows who spoke);
    ``is_agent`` flags agent senders so the UI renders their soul colour.
    """
    from skchat.models import ContentType

    sender = getattr(m, "sender", "") or ""
    ts_iso = _iso(getattr(m, "timestamp", None)) or _now_iso()
    body = getattr(m, "content", "") or ""
    outbound = sender in (OPERATOR_ID, "chef", "chef@skworld.io")
    short = sender.split(":")[-1].split("@")[0]
    is_agent = _participant_is_agent(sender)

    reactions_map = None
    if hasattr(m, "reactions_map"):
        reactions_map = m.reactions_map() or None
    receipts = None
    rcpt = getattr(m, "receipts", None)
    if rcpt is not None:
        receipts = {"delivered": list(rcpt.delivered), "read": list(rcpt.read)}
    edit_history = None
    eh = getattr(m, "edit_history", None)
    if eh:
        edit_history = [{"body": e.body, "ts": _iso(e.ts)} for e in eh]

    return {
        "id": getattr(m, "id", ""),
        "conversation_id": group_id,
        "sender": sender,
        "content_type": ContentType.to_wire(getattr(m, "content_type", "markdown")),
        "body": body,
        "rich": getattr(m, "rich", None),
        "ts": ts_iso,
        "reply_to_id": getattr(m, "reply_to_id", None),
        "thread_id": getattr(m, "thread_id", None) or group_id,
        "edited_at": _iso(getattr(m, "edited_at", None)),
        "edit_history": edit_history,
        "reactions": reactions_map,
        "receipts": receipts,
        # legacy fields
        "peer_id": group_id,
        "content": body,
        "timestamp": ts_iso,
        "is_outbound": outbound,
        "delivery_status": "sent",
        "is_encrypted": False,
        "is_agent": is_agent,
        "sender_name": "You" if outbound else (short.title() or short),
    }


def _participant_is_agent(uri: str) -> bool:
    short = (uri or "").split(":")[-1].split("@")[0].lower()
    return short in {
        "lumina", "jarvis", "opus", "ava", "ara", "artisan", "herald",
        "sentinel", "architect", "scholar", "steward", "coder",
    }


def _group_messages(group_id: str, limit: int = 500) -> list[dict]:
    """Load a group's thread (oldest-first) in the app message contract."""
    from skchat import daemon_proxy_groups as G

    hist = _get_history()
    rows = G.group_thread_messages(hist, group_id, limit=limit)
    rows.sort(key=lambda x: getattr(x, "timestamp", _now_iso()))
    return [_group_msg_to_app(m, group_id=group_id) for m in rows]


def _lumina_messages(limit: int = 200) -> list[dict]:
    """Load the operator↔Lumina thread, oldest-first, in app message shape."""
    hist = _get_history()
    rows = hist.load(peer=LUMINA_URI, limit=limit)
    rows += hist.load(peer=LUMINA_ID, limit=limit)
    rows += hist.load(peer="lumina", limit=limit)
    # Dedup by id, keep only operator↔Lumina, sort oldest-first.
    seen: set[str] = set()
    msgs: list = []
    for m in rows:
        mid = getattr(m, "id", None)
        if mid in seen:
            continue
        seen.add(mid)
        s, r = getattr(m, "sender", ""), getattr(m, "recipient", "")
        if not (_is_lumina(s) or _is_lumina(r)):
            continue
        msgs.append(m)
    msgs.sort(key=lambda x: getattr(x, "timestamp", _now_iso()))
    return [_msg_to_app(m, self_id=OPERATOR_ID) for m in msgs]


def _lumina_conversation() -> dict:
    """The pinned Lumina conversation entry (app conversation shape)."""
    msgs = _lumina_messages(limit=50)
    last = msgs[-1] if msgs else None
    return {
        "peer_id": LUMINA_ID,
        "display_name": LUMINA_NAME,
        "last_message": (last or {}).get("content", "") if last else "",
        "last_message_time": (last or {}).get("timestamp", _now_iso()),
        "soul_fingerprint": LUMINA_FINGERPRINT,
        "is_online": _operator_online(),
        "is_agent": True,
        "unread_count": 0,
        "last_delivery_status": "sent",
        "is_group": False,
        "member_count": 0,
        "avatar_url": "",
    }


def _other_peers() -> list[dict]:
    """Best-effort: known peers from ~/.skcapstone/peers/*.json (Lumina excluded).

    Lumina is added separately and pinned first; this just enriches the list.
    """
    import json

    out: list[dict] = []
    peers_dir = Path(os.path.expanduser("~/.skcapstone/peers"))
    if not peers_dir.is_dir():
        return out
    for p in sorted(peers_dir.glob("*.json")):
        try:
            data = json.loads(p.read_text())
        except Exception:
            continue
        handle = (data.get("handle") or data.get("identity") or p.stem).strip()
        if _is_lumina(handle) or _is_lumina(data.get("identity", "")) or p.stem == "lumina":
            continue
        out.append(
            {
                "peer_id": data.get("handle") or data.get("identity") or p.stem,
                "display_name": data.get("name") or p.stem.title(),
                "last_message": "",
                "last_message_time": _now_iso(),
                "soul_fingerprint": data.get("fingerprint", ""),
                "is_online": False,
                "is_agent": (data.get("agent_type") == "ai")
                or (data.get("entity_type") == "ai-agent"),
                "unread_count": 0,
                "last_delivery_status": "sent",
                "is_group": False,
                "member_count": 0,
                "avatar_url": "",
            }
        )
    return out


