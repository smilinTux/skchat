def _group_messages(group_id: str, limit: int = 500) -> list[dict]:
    """Load a group's thread (oldest-first) in the app message contract."""
    from skchat import daemon_proxy_groups as G

    hist = _get_history()
    rows = G.group_thread_messages(hist, group_id, limit=limit)
    rows.sort(key=lambda x: getattr(x, "timestamp", _now_iso()))
    return [_group_msg_to_app(m, group_id=group_id) for m in rows]


def _is_group_message(m) -> bool:
    """True only if *m* genuinely belongs to a GROUP thread, not a 1:1 DM.

    A group's per-member fan-out copy is addressed to Lumina
    (recipient=capauth:lumina) but belongs to the GROUP thread — those must
    stay out of the 1:1 Lumina DM. A plain 1:1 DM can also carry a
    client-supplied ``thread_id`` (POST /api/v1/send accepts one for
    threaded replies within the direct chat) — that is NOT a group message
    and must be kept, so ``thread_id`` alone is never sufficient evidence.
    """
    from skchat import daemon_proxy_groups as G

    meta = getattr(m, "metadata", None) or {}
    r, s = str(getattr(m, "recipient", "")), str(getattr(m, "sender", ""))
    if meta.get("group_id"):
        return True
    if r.startswith("group:") or s.startswith("group:"):
        return True
    thread_id = getattr(m, "thread_id", None)
    if thread_id and G.load_group(thread_id) is not None:
        # thread_id happens to match a real, persisted group id.
        return True
    return False


def _lumina_messages(limit: int = 200) -> list[dict]:
    """Load the operator↔Lumina thread, oldest-first, in app message shape."""
    hist = _get_history()
    rows = hist.load(peer=LUMINA_URI, limit=limit)
    rows += hist.load(peer=LUMINA_ID, limit=limit)
    rows += hist.load(peer="lumina", limit=limit)
    # Dedup by id, keep only operator↔Lumina, sort oldest-first.
    seen: set[str] = set()
    msgs: list = []
    for m in rows:
        mid = getattr(m, "id", None)
        if mid in seen:
            continue
        seen.add(mid)
        s, r = getattr(m, "sender", ""), getattr(m, "recipient", "")
        if not (_is_lumina(s) or _is_lumina(r)):
            continue
        # Exclude GROUP messages only (see _is_group_message) — a genuine
        # 1:1 DM that happens to carry a client-supplied thread_id must be
        # kept, not dropped.
        if _is_group_message(m):
            continue
        msgs.append(m)
    msgs.sort(key=lambda x: getattr(x, "timestamp", _now_iso()))
    return [_msg_to_app(m, self_id=OPERATOR_ID) for m in msgs]


def _lumina_conversation() -> dict:
    """The pinned Lumina conversation entry (app conversation shape).

    Carries BOTH the conversation-contract keys (``peer_id``/``display_name``/
    ``soul_fingerprint``, read by the Flutter app's ``Conversation.fromJson``)
    and aliased peer-contract keys (``name``/``fingerprint``). This same dict
    also backs ``GET /api/v1/peers`` (see ``api_peers``), which the app reads
    with ``PeerInfo.fromJson`` -- a DIFFERENT model that only ever looked at
    ``name``/``fingerprint``. Without the aliases, every peer discovered only
    through ``/api/v1/peers`` (no conversation thread yet) parsed to an empty
    name and a null fingerprint, and the app's fallback then substituted the
    peerId itself for the fingerprint -- which the peer-trust tier resolver
    treats as "no real key", permanently showing the peer as unverifiable
    instead of their true keyed/red tier.
    """
    msgs = _lumina_messages(limit=50)
    last = msgs[-1] if msgs else None
    return {
        "peer_id": LUMINA_ID,
        "display_name": LUMINA_NAME,
        "name": LUMINA_NAME,
        "last_message": (last or {}).get("content", "") if last else "",
        "last_message_time": (last or {}).get("timestamp", _now_iso()),
        "soul_fingerprint": LUMINA_FINGERPRINT,
        "fingerprint": LUMINA_FINGERPRINT,
        "is_online": _operator_online(),
        "is_agent": True,
        "unread_count": 0,
        "last_delivery_status": "sent",
        "is_group": False,
        "member_count": 0,
        "avatar_url": "",
    }


def _other_peers() -> list[dict]:
    """Best-effort: known peers from ~/.skcapstone/peers/*.json (Lumina excluded).

    Lumina is added separately and pinned first; this just enriches the list.

    See the ``name``/``fingerprint`` note on :func:`_lumina_conversation` --
    this dict shape backs both ``/api/v1/conversations`` and ``/api/v1/peers``,
    so it carries both key sets for the same reason.
    """
    import json

    out: list[dict] = []
    peers_dir = Path(os.path.expanduser("~/.skcapstone/peers"))
    if not peers_dir.is_dir():
        return out
    for p in sorted(peers_dir.glob("*.json")):
        try:
            data = json.loads(p.read_text())
        except Exception:
            continue
        handle = (data.get("handle") or data.get("identity") or p.stem).strip()
        if _is_lumina(handle) or _is_lumina(data.get("identity", "")) or p.stem == "lumina":
            continue
        display_name = data.get("name") or p.stem.title()
        fingerprint = data.get("fingerprint", "")
        out.append(
            {
                "peer_id": data.get("handle") or data.get("identity") or p.stem,
                "display_name": display_name,
                "name": display_name,
                "last_message": "",
                "last_message_time": _now_iso(),
                "soul_fingerprint": fingerprint,
                "fingerprint": fingerprint,
                "is_online": False,
                "is_agent": (data.get("agent_type") == "ai")
                or (data.get("entity_type") == "ai-agent"),
                "unread_count": 0,
                "last_delivery_status": "sent",
                "is_group": False,
                "member_count": 0,
                "avatar_url": "",
            }
        )
    return out


