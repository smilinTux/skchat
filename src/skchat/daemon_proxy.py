_HISTORY = None
_BRAIN = None


def _get_history():
    """Return the shared ``ChatHistory`` (JSONL/SQLite), created on first use."""
    global _HISTORY
    if _HISTORY is None:
        from skchat.history import ChatHistory

        _HISTORY = ChatHistory()
    return _HISTORY


def _get_brain():
    """Return the shared ``LuminaBrain`` (soul + qwen3.6), created on first use.

    Imports the SAME module the Telegram bridge uses (``bridge_consciousness``
    in ``scripts/``) so the app drives the identical brain — reused, not rebuilt.
    Returns ``None`` if the brain can't be constructed (degrades gracefully).
    """
    global _BRAIN
    if _BRAIN is not None:
        return _BRAIN
    try:
        scripts_dir = str(Path(__file__).resolve().parents[2] / "scripts")
        if scripts_dir not in sys.path:
            sys.path.insert(0, scripts_dir)
        from bridge_consciousness import LuminaBrain  # type: ignore

        _BRAIN = LuminaBrain(home=_AGENT_HOME, agent=LUMINA_NAME)
    except Exception:
        logger.exception("LuminaBrain unavailable — Lumina will use a fallback reply")
        _BRAIN = None
    return _BRAIN


def _persist(hist, sender: str, recipient: str, content: str, reply_to_id=None, thread_id=None):
    """Build a ChatMessage (with optional reply/thread linkage) and save it.

    ``ChatHistory.add_message`` only takes scalars, so we construct the model
    directly to carry reply_to_id/thread_id, then ``save`` it.
    """
    from skchat.models import ChatMessage

    msg = ChatMessage(
        sender=sender,
        recipient=recipient,
        content=content,
        reply_to_id=reply_to_id or None,
        thread_id=thread_id or None,
    )
    hist.save(msg)
    return msg


