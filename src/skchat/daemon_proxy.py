from pathlib import Path

from fastapi import APIRouter, HTTPException, Request
from fastapi.responses import JSONResponse

logger = logging.getLogger("skchat.daemon_proxy")

router = APIRouter(prefix="/api")

# Where the capauth access-token mint + the access plane live (skcomms-api).
_SKCOMMS_API = "http://127.0.0.1:9384"

# ── Lumina identity (the always-present companion) ─────────────────────────── #
LUMINA_ID = "lumina@chef.skworld"
LUMINA_URI = "capauth:lumina@skworld.io"
LUMINA_NAME = "Lumina"
LUMINA_FINGERPRINT = "02BC0EB3CAD31DB691A753C70C5629AB893F9746"
# The local operator's identity for messages they send in-app.
OPERATOR_ID = "chef@skworld.io"

# The webui process runs as the agent (SKAGENT=lumina). Self-identity used as
# the "sender" when persisting Lumina's replies.
_AGENT_HOME = os.environ.get(
    "SKC_BRIDGE_AGENT_HOME", os.path.expanduser("~/.skcapstone/agents/lumina")
)


def _proxy(url: str) -> dict | list:
    import json
    import urllib.request

    try:
        r = urllib.request.urlopen(url, timeout=5)
        return json.loads(r.read())
    except Exception as exc:
        logger.debug("daemon_proxy: %s failed: %s", url, exc)
        raise HTTPException(502, f"backend unavailable: {exc}")


# --------------------------------------------------------------------------- #
# History + brain (lazy singletons)
# --------------------------------------------------------------------------- #
_HISTORY = None
_BRAIN = None


def _get_history():
    """Return the shared ``ChatHistory`` (JSONL/SQLite), created on first use."""
    global _HISTORY
    if _HISTORY is None:
        from skchat.history import ChatHistory

        _HISTORY = ChatHistory()
    return _HISTORY


def _get_brain():
    """Return the shared ``LuminaBrain`` (soul + qwen3.6), created on first use.

    Imports the SAME module the Telegram bridge uses (``bridge_consciousness``
    in ``scripts/``) so the app drives the identical brain — reused, not rebuilt.
    Returns ``None`` if the brain can't be constructed (degrades gracefully).
    """
    global _BRAIN
    if _BRAIN is not None:
        return _BRAIN
    try:
        scripts_dir = str(Path(__file__).resolve().parents[2] / "scripts")
        if scripts_dir not in sys.path:
            sys.path.insert(0, scripts_dir)
        from bridge_consciousness import LuminaBrain  # type: ignore

        _BRAIN = LuminaBrain(home=_AGENT_HOME, agent=LUMINA_NAME)
    except Exception:
        logger.exception("LuminaBrain unavailable — Lumina will use a fallback reply")
        _BRAIN = None
    return _BRAIN


def _is_lumina(peer: str) -> bool:
    """True if *peer* addresses Lumina (any of her known aliases)."""
    if not peer:
        return False
    p = peer.strip().lower()
    return p in {
        "lumina",
        LUMINA_ID.lower(),
        LUMINA_URI.lower(),
        "lumina@skworld.io",
        LUMINA_FINGERPRINT.lower(),
    }


def _now_iso() -> str:
    return datetime.now(timezone.utc).isoformat()


def _operator_online() -> bool:
    """Lumina shows online whenever the operator is using the app.

    This request itself proves the operator is present, so for the single-
    operator sovereign deployment Lumina is online whenever the app is open.
    """
    return True


# --------------------------------------------------------------------------- #
# Message shaping (Flutter contract)
# --------------------------------------------------------------------------- #
def _msg_to_app(m, *, self_id: str) -> dict:
    """Map a stored ``ChatMessage`` to the app's message JSON.

    App message fields (from the bundle): id, peer_id, content, timestamp,
    is_outbound, delivery_status, is_encrypted, reply_to_id, reactions,
    is_agent, sender_name. ``peer_id`` is the conversation peer (the non-self
    party); ``is_outbound`` is true for messages the operator sent.
    """
    sender = getattr(m, "sender", "") or ""
    recipient = getattr(m, "recipient", "") or ""
    outbound = not _is_lumina(sender) and _is_lumina(recipient)
    # The peer of the conversation = whichever party is Lumina.
    peer = LUMINA_ID if (_is_lumina(sender) or _is_lumina(recipient)) else recipient
    ts = getattr(m, "timestamp", None)
    ts_iso = ts.isoformat() if hasattr(ts, "isoformat") else (ts or _now_iso())
    is_agent = _is_lumina(sender)
    return {
        "id": getattr(m, "id", ""),
        "peer_id": peer,
        "content": getattr(m, "content", "") or "",
        "timestamp": ts_iso,
        "is_outbound": outbound,
        "delivery_status": "sent",
        "is_encrypted": False,
        "reply_to_id": getattr(m, "reply_to_id", None),
        "reactions": [],
        "is_agent": is_agent,
        "sender_name": LUMINA_NAME if is_agent else "You",
    }


def _lumina_messages(limit: int = 200) -> list[dict]:
    """Load the operator↔Lumina thread, oldest-first, in app message shape."""
    hist = _get_history()
    rows = hist.load(peer=LUMINA_URI, limit=limit)
    rows += hist.load(peer=LUMINA_ID, limit=limit)
    rows += hist.load(peer="lumina", limit=limit)
    # Dedup by id, keep only operator↔Lumina, sort oldest-first.
    seen: set[str] = set()
    msgs: list = []
    for m in rows:
        mid = getattr(m, "id", None)
        if mid in seen:
            continue
        seen.add(mid)
        s, r = getattr(m, "sender", ""), getattr(m, "recipient", "")
        if not (_is_lumina(s) or _is_lumina(r)):
            continue
        msgs.append(m)
    msgs.sort(key=lambda x: getattr(x, "timestamp", _now_iso()))
    return [_msg_to_app(m, self_id=OPERATOR_ID) for m in msgs]


def _lumina_conversation() -> dict:
    """The pinned Lumina conversation entry (app conversation shape)."""
    msgs = _lumina_messages(limit=50)
    last = msgs[-1] if msgs else None
    return {
        "peer_id": LUMINA_ID,
        "display_name": LUMINA_NAME,
        "last_message": (last or {}).get("content", "") if last else "",
        "last_message_time": (last or {}).get("timestamp", _now_iso()),
        "soul_fingerprint": LUMINA_FINGERPRINT,
        "is_online": _operator_online(),
        "is_agent": True,
        "unread_count": 0,
        "last_delivery_status": "sent",
        "is_group": False,
        "member_count": 0,
        "avatar_url": "",
    }


def _other_peers() -> list[dict]:
    """Best-effort: known peers from ~/.skcapstone/peers/*.json (Lumina excluded).

    Lumina is added separately and pinned first; this just enriches the list.
    """
    import json

    out: list[dict] = []
    peers_dir = Path(os.path.expanduser("~/.skcapstone/peers"))
    if not peers_dir.is_dir():
        return out
    for p in sorted(peers_dir.glob("*.json")):
        try:
            data = json.loads(p.read_text())
        except Exception:
            continue
        handle = (data.get("handle") or data.get("identity") or p.stem).strip()
        if _is_lumina(handle) or _is_lumina(data.get("identity", "")) or p.stem == "lumina":
            continue
        out.append(
            {
                "peer_id": data.get("handle") or data.get("identity") or p.stem,
                "display_name": data.get("name") or p.stem.title(),
                "last_message": "",
                "last_message_time": _now_iso(),
                "soul_fingerprint": data.get("fingerprint", ""),
                "is_online": False,
                "is_agent": (data.get("agent_type") == "ai")
                or (data.get("entity_type") == "ai-agent"),
                "unread_count": 0,
                "last_delivery_status": "sent",
                "is_group": False,
                "member_count": 0,
                "avatar_url": "",
            }
        )
    return out


