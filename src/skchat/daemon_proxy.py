"""Daemon API proxy — serves the endpoints the Flutter app needs by stitching
together skchat daemon, skcapstone API, and webui behind a single /api/* prefix.

Chat surface (the part the Flutter app's chat UI reads):
  * GET  /api/v1/peers          — contacts; **Lumina is always present + first**
  * GET  /api/v1/conversations  — conversation list; Lumina pinned online
  * GET  /api/v1/inbox          — every message (app groups by peer_id)
  * GET  /api/v1/conversations/{peer_id} — one thread, oldest-first
  * POST /api/v1/send           — persists the message and, for Lumina, invokes
                                   her real brain (soul + qwen3.6) and returns
                                   + persists her reply.

History is backed by the real ``skchat.history.ChatHistory`` (JSONL/SQLite) so
the Lumina thread survives reloads. Lumina's reply is generated by the SAME
``LuminaBrain`` the Telegram bridge drives (``scripts/bridge_consciousness``) —
the genuine ``lumina-unhinged`` soul + FEB on qwen3.6, not a generic assistant.
"""

from __future__ import annotations

import logging
